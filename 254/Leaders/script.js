$(document).ready(function() {
    $('#2017-2018').show();
    $('#2016-2017').hide();
    $('#2015-2016').hide();

    $('#2017-2018 .prev').click(function() {
        $('#2017-2018').hide();
        $('#2016-2017').show();
    });

    $('#2016-2017 .next').click(function() {
        $('#2017-2018').show();
        $('#2016-2017').hide();
    });

    $('#2016-2017 .prev').click(function() {
        $('#2016-2017').hide();
        $('#2015-2016').show();
    });
});